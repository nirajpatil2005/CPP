#include<iostream>
using namespace std;
int main()
{
    // increment and decrement 
    int x= 10;
    x++; //postincrement
    cout<<x<<endl;
    int y =10;
    y--; //postdecrement
    cout<<y<<endl;
    int z =10;
    ++z; //preincrement
    cout<<z<<endl;
    --z; //predecrement
    cout<<z<<endl;
    return 0;

}